package aula;

public class Calculos {
	
	public double soma(double n, double b){
		double total;
		return total = n + b;
	}
	public double div(double x, double y){
		double total;
		return total = x / y;
	}
	public double subtr(double c, double d){
		double total;
		return total = c - d;
	}
	public double mult(double s, double t){
		double total;
		return total = s * t;
	}
	public double raizq(double s){
		double total;
		return total = Math.sqrt(s);
	}
}
